■制空値とは：
「制空値」とは海域における航空戦力の拮抗状況に影響するもので、味方出撃艦隊と出撃海域それぞれに「制空値」があり、味方の制空値と敵制空値との比較によって「制空権」が決定されます。

「制空権」の状況により、下記の項目に影響があります。
・味方の航空攻撃が与えるダメージ
・味方艦隊が受ける敵航空攻撃によるダメージ
・味方艦隊の一部ステータス（命中・回避・偵察値が微増/減）

また、味方艦隊と海域の制空値が両方一定の数値より低い場合、「制空権」による影響が無効になります。
制空権の効果は海域マップ画面上部のアイコンをタップすることで確認することが出来ます。

■制空値の決め方
味方艦隊の制空値は下記の項目を参照します。
・味方艦隊の制空艦（正規空母、軽空母、航空戦艦、潜水空母）数
・味方艦隊の「航空」ステータス
・味方艦隊の搭載中の艦載機（戦闘機・爆撃機・攻撃機・水上機）
味方艦隊の初期制空値は海域マップ選択画面の出撃準備画面で確認することが出来ます。

海域制空値は下記の項目を参照します。
・海域深度（章数）
・味方艦隊の「対空」ステータスハードモードにおける海域制空値は「周回モード」の設定により変化します。

■tips
・味方艦隊の現在の制空値は「海域マップ画面で戦闘可能な艦」によって決められます。
・味方艦隊が直接戦闘に参加しなくても、制空値に貢献することができます。
・味方制空艦が戦闘不能になると、その艦の分だけ味方艦隊の制空値が低下します。]]

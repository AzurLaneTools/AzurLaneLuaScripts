■概要
「花火工場」では、自分の花火を制作するミニゲーム
になります。
好きな火薬を選び、装薬量が所定の適切値になるよう
装薬し、装薬値が所定の適切値に近ければ近いほど、
より良い景品がもらえます。
また、「花火工場」で制作した花火は、イベントぺー
ジ画面の「打ち上げ」をタップすることで打ち上げる
ことが出来ます。
打ち上げられた花火はイベントページから離れる
まで表示されます。
■操作方法
画面下部の「装薬開始」をタップすると、花火の制作
を開始できます。
①火薬を選ぼう
画面の右側にある、花火の星の色を合計3種類任意に選
びます。
火薬玉（星）の並び順によって花火の親星と芯星の色
が変わりますので、要注意！
「装薬完了」をタップすると火薬の種類の選択が完了
します。
②火薬を装填しよう
画面下部の「開始」をタップすると、花火の装填量を
決めることが出来ます。
開始ボタンをタップすると装填が開始し、もう一回ボ
タンをタップすると装薬が終了します。
装填が3回行われると、花火の制作が完了します。
1回の装填量は50％のライン（緑ライン）に近いほど
装薬量が適切になり、3回の合計装填料が150%に近い
ほど、より良い景品がもらえます。
③花火を打ち上げよう
花火の制作が完了すると、イベントページで
「打ち上げ」ボタンが表示されます。
「打ち上げ」ボタンをタップすると、制作した花火が
イベントページの背景に打ち上がります。]]
